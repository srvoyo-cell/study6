def integral_func(x):
    inner_func = lambda t: np.cos(x + 5 * t) * np.sin(2 * x - t)
    integ = NumericalIntegration(inner_func)
    res, n, err, depen = integ.runge_rule(
        method="Симпсон", lower_limit=0, upper_limit=3, tol=1e-2
    )
    return res, n, err, depen


